// Mapping of common category names to icons
const iconMap = {
    'business': <BusinessIcon />,
    'professional skills': <SkillsIcon />,
    'relationship': <RelationshipIcon />,
    'lifestyle': <LifestyleIcon />,
    'education': <EducationIcon />,
    'technology': <TechnologyIcon />,
    'health': <HealthIcon />,
    'arts': <ArtsIcon />,
};

// Function to get icon by category name
export const getCategoryIcon = (categoryName) => {
    if (!categoryName) return <DefaultIcon />;
    
    const lowerCaseName = categoryName.toLowerCase();
    
    // Check for exact matches
    if (iconMap[lowerCaseName]) {
        return iconMap[lowerCaseName];
    }
    
    // Check for partial matches
    for (const [key, icon] of Object.entries(iconMap)) {
        if (lowerCaseName.includes(key)) {
            return icon;
        }
    }
    
    // Return default icon if no match
    return <DefaultIcon />;
};

export default {
    getCategoryIcon,
}; 